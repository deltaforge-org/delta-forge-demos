-- ============================================================================
-- Setup: Delta Time-Based Partitioning — Monthly Sensor Data
-- ============================================================================
-- Creates a partitioned Delta table of factory sensor readings organized by
-- year_month. Five sensors across two production lines report temperature
-- readings three times per month over six months (Jan-Jun 2024).
-- ============================================================================

CREATE ZONE IF NOT EXISTS {{zone_name}} WITH DATA_PATH = '{{data_path}}';
CREATE SCHEMA IF NOT EXISTS {{zone_name}}.delta_demos;

CREATE DELTA TABLE IF NOT EXISTS {{zone_name}}.delta_demos.line_metrics (
    id              INT,
    sensor_id       VARCHAR,
    reading         DOUBLE,
    unit            VARCHAR,
    line_name       VARCHAR,
    recorded_at     VARCHAR,
    year_month      VARCHAR
) PARTITIONED BY (year_month)
  LOCATION '{{data_path}}/delta_demos/line_metrics';

GRANT ADMIN ON {{zone_name}}.delta_demos.line_metrics;

