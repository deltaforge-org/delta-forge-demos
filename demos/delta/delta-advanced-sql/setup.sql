-- ============================================================================
-- Delta Advanced SQL — Setup Script
-- ============================================================================
-- Creates a stock_prices table with 5 stocks × 20 trading days = 100 rows.
-- All prices are deterministic for verifiable analytics.
--
-- Stocks: AAPL, MSFT, GOOGL, AMZN, TSLA
-- Dates: 2024-01-02 through 2024-01-29 (20 trading days)
--
-- Price formula (deterministic):
--   base_price per stock, daily change via sine wave for smooth trends
--   open  = base + amplitude * sin(day * 0.3)
--   close = open + small daily shift
--   high  = max(open, close) + small positive offset
--   low   = min(open, close) - small positive offset
--   volume = base_volume + volume variation
-- ============================================================================

-- STEP 1: Zone & Schema
CREATE ZONE IF NOT EXISTS {{zone_name}} TYPE EXTERNAL
    COMMENT 'External and Delta tables — demo datasets';

CREATE SCHEMA IF NOT EXISTS {{zone_name}}.delta_demos
    COMMENT 'Delta table management tutorial demos';


-- ============================================================================
-- TABLE: stock_prices — 100 rows (5 stocks × 20 days)
-- ============================================================================
CREATE DELTA TABLE IF NOT EXISTS {{zone_name}}.delta_demos.stock_prices (
    symbol      VARCHAR,
    trade_date  VARCHAR,
    open_price  DOUBLE,
    close_price DOUBLE,
    high_price  DOUBLE,
    low_price   DOUBLE,
    volume      BIGINT
) LOCATION '{{data_path}}/stock_prices';

GRANT ADMIN ON TABLE {{zone_name}}.delta_demos.stock_prices TO USER {{current_user}};

