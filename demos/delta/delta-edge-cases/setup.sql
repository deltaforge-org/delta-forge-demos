-- ============================================================================
-- Delta Edge Cases — Empty, Wide & Minimal Tables — Setup Script
-- ============================================================================
-- Demonstrates Delta table edge cases:
--   - Singleton configuration table (1 row, multiple updates)
--   - Wide table with 30 columns
--   - Empty table (schema only, 0 rows)
--
-- Tables created:
--   1. config_singleton — single-row config (baseline version 1)
--   2. wide_metrics    — 30 columns of KPI data
--   3. empty_staging   — schema-only empty table
-- ============================================================================

-- STEP 1: Zone & Schema
CREATE ZONE IF NOT EXISTS {{zone_name}} TYPE EXTERNAL
    COMMENT 'External and Delta tables — demo datasets';

CREATE SCHEMA IF NOT EXISTS {{zone_name}}.delta_demos
    COMMENT 'Delta table management tutorial demos';


-- ============================================================================
-- TABLE 1: config_singleton — single-row configuration
-- ============================================================================
CREATE DELTA TABLE IF NOT EXISTS {{zone_name}}.delta_demos.config_singleton (
    config_key      VARCHAR,
    config_value    VARCHAR,
    version         INT,
    updated_by      VARCHAR,
    updated_at      VARCHAR
) LOCATION 'config_singleton';


